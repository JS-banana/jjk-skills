# 综合评分计算
def calculate_priority(strength: str, universality: str, solvability: str, match: str) -> str:
    """
    计算优先级
    
    综合评分 = 需求强度(强=3/中=2/弱=1) × 需求普遍性(高=3/中=2/低=1) × 需求可解决性(高=3/中=2/低=1) × 匹配度(高=3/中=2/低=1)
    
    优先级规则：
    - ≥81: P0-立即行动
    - 54-80: P1-重点考虑
    - 27-53: P2-观察中
    - <27: P3-待观察
    """
    strength_map = {"强": 3, "中": 2, "弱": 1}
    universality_map = {"高": 3, "中": 2, "低": 1}
    solvability_map = {"高": 3, "中": 2, "低": 1}
    match_map = {"高": 3, "中": 2, "低": 1}
    
    score = (
        strength_map.get(strength, 1) *
        universality_map.get(universality, 1) *
        solvability_map.get(solvability, 1) *
        match_map.get(match, 1)
    )
    
    if score >= 81:
        return "P0-立即行动"
    elif score >= 54:
        return "P1-重点考虑"
    elif score >= 27:
        return "P2-观察中"
    else:
        return "P3-待观察"


